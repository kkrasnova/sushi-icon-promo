import type { Config } from "tailwindcss";
import defaultTheme from "tailwindcss/defaultTheme";
import flattenColorPalette from "tailwindcss/lib/util/flattenColorPalette";
import tailwindcssAnimate from "tailwindcss-animate";
import plugin from "tailwindcss/plugin";

const addVariablesForColors = plugin(({ addBase, theme }) => {
  const allColors = flattenColorPalette(theme("colors")) as Record<string, string>;
  const newVars: Record<string, string> = {};

  for (const colorName in allColors) {
    if (Object.prototype.hasOwnProperty.call(allColors, colorName)) {
      newVars[`--${colorName}`] = allColors[colorName];
    }
  }

  addBase({
    ":root": newVars,
  });
});

